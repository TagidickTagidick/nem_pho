  @override
  void initState() {
    searchStreets = streets;
    super.initState();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        elevation: 0,
        backgroundColor: Colors.white,
        leading: const BackButton(
          color: Colors.black,
        ),
      ),
      body: Column(
        children: [
          Container(
            margin: const EdgeInsets.symmetric(horizontal: 10),
            padding: const EdgeInsets.only(left: 22),
            decoration: BoxDecoration(
              color: const Color(0xffFFFFFF),
              borderRadius: BorderRadius.circular(200),
              border: Border.all(
                color: const Color(0xffF0B0B0),
              ),
            ),
            child: TextField(
              controller: _controller,
              keyboardType: TextInputType.streetAddress,
              onChanged: (value) {
                searchStreets = [];
                for (int i = 0; i < streets.length; i++) {
                  if (streets[i].toLowerCase().contains(value)) {
                    searchStreets.add(streets[i]);
                  }
                }
                if (streets.isEmpty) {
                  searchStreets = streets;
                }
                setState(() {});
              },
              decoration: InputDecoration(
                border: InputBorder.none,
                hintText: "Укажите улицу и район",
                hintStyle: TextStyle(
                  fontWeight: FontWeight.w400,
                  fontSize: 16,
                  color: const Color(0xff000000).withOpacity(0.42),
                ),
              ),
            ),
          ),
          Expanded(
            child: ListView.builder(
              itemCount: searchStreets.length,
              itemBuilder: (context, index) => GestureDetector(
                onTap: () => Navigator.of(context).pop(searchStreets[index]),
                child: Container(
                  color: Colors.transparent,
                  padding: const EdgeInsets.symmetric(
                    horizontal: 5,
                    vertical: 5,
                  ),
                  child: Text(searchStreets[index]),
                ),
              ),
            ),
          )
        ],
      ),
    );
  }
